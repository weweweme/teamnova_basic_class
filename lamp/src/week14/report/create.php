<?php
// ============================================================
// report/create.php — 신고 처리  [POST 요청 → PRG]
//   글 보기 화면의 신고 폼이 보낸 '글 번호 + 신고 사유'를 받아
//   (지금은 접수된 셈 치고) 그 글로 다시 리다이렉트한다.
// ============================================================
require_once __DIR__ . '/../includes/util.php';

// 허용된 신고 사유 목록.
//   ★ 왜 목록으로 정해두나? 폼의 <select>는 화면상 4개뿐이지만,
//     사용자가 개발자도구로 값을 바꾸거나 직접 요청을 만들면 '아무 값'이나 보낼 수 있다.
//     그래서 서버에서 '허용된 것만 인정'하는 화이트리스트 검증이 필요하다.
//     (클라이언트 화면은 못 믿고, 서버 검증이 진짜라는 원칙)
const ALLOWED_REASONS = ['스팸/광고', '욕설/비방', '허위정보', '기타'];

// ── 0) POST로 온 게 맞나? ────────────────────────────────────
if ($_SERVER['REQUEST_METHOD'] !== 'POST') {
    header('Location: /');
    exit;
}

// ── 1) 값 받기 ───────────────────────────────────────────────
$postId = (int)($_POST['post_id'] ?? 0);
$reason = $_POST['reason'] ?? '';

// ── 2) 검증 ──────────────────────────────────────────────────
if ($postId <= 0) {
    header('Location: /');
    exit;
}
// 사유가 허용 목록에 없으면 신고 처리하지 않고 그 글로 돌려보낸다.
if (!in_array($reason, ALLOWED_REASONS, true)) {
    header("Location: /post/view.php?id=$postId");
    exit;
}

// ── 3) 저장 (지금은 stub) ────────────────────────────────────
//   나중엔 reports 테이블에 (post_id, reason, 신고자, 시각)을 INSERT 한다.

// ── 4) PRG: 그 글로 다시 리다이렉트 (+접수 완료 표시) ────────
header("Location: /post/view.php?id=$postId&reported=1");
exit;
