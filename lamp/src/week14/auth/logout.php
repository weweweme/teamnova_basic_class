<?php
// ============================================================
// auth/logout.php — 로그아웃 처리  [POST 요청 → PRG]
//
//   ★ 왜 로그아웃이 링크(GET)가 아니라 POST인가?
//     로그아웃은 '상태를 바꾸는' 동작이다. GET 링크로 만들면
//     브라우저의 미리 불러오기나 남이 심어둔 <img src="/auth/logout.php">만으로도
//     나도 모르게 로그아웃당할 수 있다. (삭제를 POST로 한 것과 같은 이유)
// ============================================================
require_once __DIR__ . '/../includes/util.php';
require_once __DIR__ . '/../includes/auth.php';

// ── 0) POST로 온 게 맞나? ────────────────────────────────────
if ($_SERVER['REQUEST_METHOD'] !== 'POST') {
    header('Location: /');
    exit;
}

// ── 1) 세션 비우기 = 로그아웃 ────────────────────────────────
logout_user();

// ── 2) PRG: 홈으로 ───────────────────────────────────────────
header('Location: /');
exit;
